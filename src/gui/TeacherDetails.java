    public void viewData()
    {
        try
        {
            jTable1.setShowGrid(true);
            DBConnection db=new DBConnection();
            String sql="select * from teacher";
            ResultSet rs=db.s.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
             
            while(rs.next())
            {
                model.addRow(new Object[]{rs.getString("name"),rs.getString("employeeid"),rs.getString("email"),rs.getString("dob"),rs.getString("contact"),rs.getString("education"),rs.getString("department"),rs.getString("gender"),rs.getString("graduation"),rs.getString("address")});
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    
