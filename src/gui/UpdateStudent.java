    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try
        {
            DBConnection db=new DBConnection();
            String roll=jTextField9.getText();
            String sql="select * from student where rollno='"+roll+"'";
            ResultSet rs=db.s.executeQuery(sql);
            
            while(rs.next())
            {
                jTextField1.setText(rs.getString("name"));
                jTextField2.setText(rs.getString("rollno"));
                jTextField3.setText(rs.getString("email"));
                jTextField8.setText(rs.getString("dob"));
                jTextField4.setText(rs.getString("contact"));
                jTextField10.setText(rs.getString("course"));
                jTextField11.setText(rs.getString("branch"));
                if(rs.getString("gender").equals("Male"))
                    jRadioButton1.setSelected(true);
                else if(rs.getString("gender").equals("Female"))
                    jRadioButton2.setSelected(true);
                else if(rs.getString("gender").equals("Others"))
                    jRadioButton3.setSelected(true);
                jTextField5.setText(rs.getString("tenth"));
                jTextField6.setText(rs.getString("twelfth"));
                jTextField7.setText(rs.getString("address"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

