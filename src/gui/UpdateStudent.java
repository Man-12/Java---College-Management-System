    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name=jTextField1.getText();
        String rollno=jTextField2.getText();
        String email=jTextField3.getText();
        String dob=jTextField8.getText();
        String contact=jTextField4.getText();
        String course=jTextField10.getText();
        String branch=jTextField11.getText();
        String gender="";

        if(jRadioButton1.isSelected())
        {
            gender="Male";
        }
        else if(jRadioButton2.isSelected())
        {
            gender="Female";
        }
        else if(jRadioButton3.isSelected())
        {
            gender="Other";
        }

        String tenth=jTextField5.getText();
        String twelfth=jTextField6.getText();
        String address=jTextField7.getText();

        try
        {
            DBConnection db=new DBConnection();
            String q="update student set name='"+name+"',rollno='"+rollno+"',email='"+email+"',dob='"+dob+"',contact='"+contact+"',course='"+course+"',branch='"+branch+"',gender='"+gender+"',tenth='"+tenth+"',twelfth='"+twelfth+"',address='"+address+"'where rollno='"+jTextField9.getText()+"'";
            db.s.executeUpdate(q);
            JOptionPane.showMessageDialog(this, "Student Details Updated Succesfully!");
            this.setVisible(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

