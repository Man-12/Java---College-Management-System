    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fee=Integer.parseInt(jTextField4.getText());
        int paidfee=fee + Integer.parseInt(jTextField3.getText());
        jTextField3.setText(""+paidfee);
        int remainingfee=Integer.parseInt(jTextField1.getText())-paidfee;
        jTextField2.setText(""+remainingfee);
        jTextField4.setText("");
        
        try
        {   DBConnection db=new DBConnection();
            String sql ="update fee set paidfee="+paidfee+",remainingfee="+remainingfee+" where rollno='"+jComboBox1.getSelectedItem().toString()+"' and semester='"+jComboBox2.getSelectedItem().toString()+"'";
            db.s.executeUpdate(sql);
            JOptionPane.showMessageDialog(this,"Fee Added Successfully");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

