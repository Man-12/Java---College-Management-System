    public void comb()
    {
        try
        {
            DBConnection db=new DBConnection();
            ResultSet rs=db.s.executeQuery("select * from student");
            while(rs.next())
            {
                jComboBox1.addItem(rs.getString("rollno"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    } 
    public void view()
    {
        try
        {    
            DBConnection db=new DBConnection();
            ResultSet rs=db.s.executeQuery("select * from fee where rollno='"+jComboBox1.getSelectedItem().toString()+"' and semester='"+jComboBox2.getSelectedItem().toString()+"'");
            
            while(rs.next())
            {
                jTextField1.setText(rs.getString("totalfee"));
                jTextField2.setText(rs.getString("remainingfee"));
                jTextField3.setText(rs.getString("paidfee"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    

